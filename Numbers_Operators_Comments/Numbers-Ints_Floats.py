# Two main types of numbers: #
    # Integers vs Floating Points
# Integer:
    # 4, 57, -10
# Float:
    # 6.1, 1.3333, 0.0
# type() tells us if the number is a float or an integer
    # type(9) int
    # type(9.0) float
# if any part of an expression is a float, the end result will be a float
    # ex: 1+1.0 = 2.0

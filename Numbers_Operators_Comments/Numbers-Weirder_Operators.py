# "**" = Exponentiation
    # 2**3 = 8
    # Square root: 49 ** 0.5 = 7 
# "%" = Modulo
    # Known as the remainder operator
    # Takes two numbers, divides them into each other as many times as possible without being fractional, giving us the remainder.
    # 10 % 3 = 1 -> 3 goes into 10 3 times, with 1 left over
    # 25 % 4 = 1 -> 4 goes into 25 6 times, with 1 left over.
    # Modulo commonly used to determine if numbers are even or odd. Will potentially use this operation.
# "//" = Integer Division
    # Integer division always rounds down
    # 10/3 = 3.3333 but 10//3 rounds down to 3
    # 6//7 = 0 

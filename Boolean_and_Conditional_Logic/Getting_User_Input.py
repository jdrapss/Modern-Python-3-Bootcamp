## User Input ##
# Built in function called input. Prompts user to enter something from the command line.
# Results from user input are stored in a variable
# Ex:
name = input("Enter your name here: ") # Input provided on same line
print("You said " + name) # "You said Pax" 


# Using print to pass the initial string
print("Whats your favorite color?") # Prompts input on a separate line
color = input()
print("You said " + color) 


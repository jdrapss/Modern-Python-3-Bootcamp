## String Concatenation Exercise ##
# Set the variable called "greeting" to some greeting
greeting = "Howdy"
# Set the variable called "name" to some name
name = "Doug Dimmadome"
# Set the variable called "greet_name" so that it concatenates greeting, name, and a space " " between them.
greet_name = greeting + " " + name + "."
print(greet_name) # Howdy Doug Dimmadome